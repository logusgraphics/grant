/**
 * Metadata for env vars: categories, priority (main vs optional), descriptions, and dependencies.
 * Used to drive the config UI tabs and status.
 */

export type EnvCategoryId =
  | 'main'
  | 'database'
  | 'cache'
  | 'auth'
  | 'github'
  | 'security'
  | 'optional';

export interface EnvVarMeta {
  key: string;
  category: EnvCategoryId;
  /** Human-readable label */
  label: string;
  /** Short description for the UI */
  description: string;
  /** If set, this var is only relevant when the condition var has one of these values (e.g. REDIS_* when CACHE_STRATEGY=redis) */
  dependsOn?: { key: string; values: string[] };
  /** Whether this var is required for the app to work in production */
  required?: boolean;
  /** Which env file(s) this var is written to. Key = relative path from repo root. */
  envFiles: string[];
  /** If true, changing this var should sync to all listed envFiles (e.g. DB_URL in database + api) */
  syncAcrossFiles?: boolean;
  /** If set, render a selector with these options instead of a text input */
  options?: readonly string[];
  /** When options are set: open dropdown above the trigger (e.g. for fields at bottom of panel). Default is below. */
  dropdownPosition?: 'above' | 'below';
  /** If true, input only allows digits (e.g. ports) */
  digitsOnly?: boolean;
  /** If true, show a "Generate password" button below the field */
  isPassword?: boolean;
  /** If set (e.g. ','), render multiple inputs and join with this separator when persisting */
  multiValueSeparator?: string;
  /** Optional section label for grouping vars in the UI (e.g. "Rate limiting", "Row-Level Security") */
  section?: string;
  /** If true, this var is "critical" for the section: section is shown first and expanded by default; var is shown first within section */
  critical?: boolean;
}


export function getEnvVarMeta(key: string): EnvVarMeta | undefined {
  return META_BY_KEY.get(key);
}

export function getAllEnvVarMeta(): EnvVarMeta[] {
  return [...META];
}

export function getMetaByCategory(category: EnvCategoryId): EnvVarMeta[] {
  return META.filter((m) => m.category === category);
}

export function getSyncTargetsForKey(key: string): string[] {
  const meta = getEnvVarMeta(key);
  return meta?.envFiles ?? ['.env'];
}
